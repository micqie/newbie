        <h2>Volleyball</h2>
        <p>
            Volleyball is a fast-paced sport played by two teams of six players. Each team tries to score points by grounding the ball on the opponent's court. 
            It requires teamwork, agility, and strategy. Originating in 1895, volleyball has become one of the most popular sports worldwide.
        </p>
        <p>
            <strong>Did you know?</strong> Beach volleyball, played on sand, became an Olympic sport in 1996.
        </p>
