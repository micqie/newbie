        <h2>Basketball</h2>
        <p>
            Basketball is a dynamic game played by two teams of five players. The goal is to score points by shooting the ball through the opponent's hoop. 
            Invented in 1891, basketball has grown to become a global sport, with leagues like the NBA showcasing incredible talent and athleticism.
        </p>
        <p>
            <strong>Interesting Fact:</strong> Michael Jordan, widely regarded as the greatest basketball player, popularized the sport in the 1990s.
